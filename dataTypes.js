// typeof identifies the type of data in the variable

// Primitive Data Types

// Numbers
const day = 5;
console.log(typeof(day));
// Strings
const name = 'Eric';
console.log(typeof(name));
//Booleans
const isLogged = true;
console.log(typeof(isLogged));
//Undefined -- basically, nothing assigned
let date;
// Null -- when a value is explicitly nothing
let minute = null;
console.log(typeof(minute));
//Symbol
const hour = Symbol();
console.log(typeof(hour));
